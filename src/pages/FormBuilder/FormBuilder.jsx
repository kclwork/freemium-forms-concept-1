// ── Step 4: Review & Email Gate ───────────────────────────────────────────────

function Step4({ form, update, onBack, onEditStep, onSubmit }) {
  const [emailError, setEmailError] = useState('')

  const rows = [
    { label: 'Disclosing party', value: form.disclosingParty, editStep: 1 },
    { label: 'Receiving party',  value: form.receivingParty,  editStep: 1 },
    { label: 'Relationship',     value: form.relationship,    editStep: 1 },
    { label: 'Effective date',   value: form.startDate,       editStep: 1 },
    { label: "What's protected", value: form.infoTypes.length ? form.infoTypes.join(', ') : form.description, editStep: 2 },
    { label: 'Duration',         value: form.duration,        editStep: 2 },
    { label: 'Jurisdiction',     value: form.governingState,  editStep: 3 },
  ]

  function handleSubmit() {
    if (!isValidEmail(form.email)) {
      setEmailError('Please enter a valid email address')
      return
    }
    setEmailError('')
    onSubmit()
  }

  return (
    <div className={styles.stepContent}>
      <h2 className={styles.stepHeading}>Your NDA is ready to download.</h2>
      <p className={styles.stepSub}>Take a moment to review your details below. Everything look right? Enter your email to get your completed NDA.</p>

      {/* Summary card */}
      <div className={styles.reviewCard}>
        <p className={styles.reviewCardLabel}>Review your details</p>
        <div className={styles.reviewTable}>
          {rows.map(row => (
            <div className={styles.reviewRow} key={row.label}>
              <span className={styles.reviewFieldLabel}>{row.label}</span>
              <span className={styles.reviewValue}>{row.value || <span className={styles.reviewEmpty}>—</span>}</span>
              <button className={styles.editBtn} onClick={() => onEditStep(row.editStep)}>Edit</button>
            </div>
          ))}
        </div>
      </div>

      {/* Email gate */}
      <div className={styles.emailCard}>
        <h3 className={styles.emailCardHeading}>Your NDA is ready.</h3>
        <p className={styles.emailCardSub}>Enter your email and we'll send you a copy and unlock your download.</p>
        <div className={`${styles.emailInputRow} ${emailError ? styles.emailInputRowError : ''}`}>
          <input
            type="email"
            className={styles.emailInput}
            placeholder="Email address"
            value={form.email}
            onChange={e => { update('email', e.target.value); if (emailError) setEmailError('') }}
            onKeyDown={e => e.key === 'Enter' && handleSubmit()}
          />
          <button className={styles.emailSubmitBtn} onClick={handleSubmit} aria-label="Submit">→</button>
        </div>
        {emailError && <p className={styles.emailError}>{emailError}</p>}
        <p className={styles.emailDisclaimer}>
          By continuing you agree to receive emails from LegalShield. View our Privacy Policy. Legal language is provided by LegalShield attorneys and may not be edited without a paid plan.
        </p>
      </div>

      <div className={styles.stepCtas}>
        <button className={styles.btnBack} onClick={onBack}>← Back</button>
      </div>
    </div>
  )
}

// ── Main Component ────────────────────────────────────────────────────────────

export default function FormBuilder() {
  const navigate = useNavigate()
  const [step, setStep] = useState(1)
  const [form, setForm] = useState(INITIAL_FORM)
  const topRef = useRef(null)

  useEffect(() => {
    topRef.current?.scrollIntoView({ behavior: 'smooth', block: 'start' })
  }, [step])

  function update(field, value) {
    setForm(prev => ({ ...prev, [field]: value }))
  }

  function goBack() {
    if (step === 1) navigate('/forms/nda')
    else setStep(s => s - 1)
  }

  function goNext() {
    setStep(s => s + 1)
  }

  function editStep(n) {
    setStep(n)
  }

  function handleSubmit() {
    triggerPDFDownload(form.disclosingParty, form.receivingParty)
    navigate('/forms/nda/confirmation', { state: { email: form.email, disclosingParty: form.disclosingParty, receivingParty: form.receivingParty } })
  }

  return (
    <div className={styles.page}>
      <Nav />

      {/* Page header — full width */}
      <div className={styles.pageHeader} ref={topRef}>
        <div className={styles.pageHeaderInner}>
          <p className={styles.eyebrow}>BUSINESS — CONFIDENTIALITY</p>
          <h1 className={styles.headline}>Non-disclosure agreement (NDA)</h1>
          <Stepper step={step} />
        </div>
      </div>

      {/* Two-column layout */}
      <div className={styles.twoColWrap}>
        <div className={styles.twoColInner}>
          <div className={styles.leftCol}>
            {step === 1 && <Step1 form={form} update={update} onBack={goBack} onNext={goNext} />}
            {step === 2 && <Step2 form={form} update={update} onBack={goBack} onNext={goNext} />}
            {step === 3 && <Step3 form={form} update={update} onBack={goBack} onNext={goNext} />}
            {step === 4 && <Step4 form={form} update={update} onBack={goBack} onEditStep={editStep} onSubmit={handleSubmit} />}
          </div>
          <div className={styles.rightCol}>
            <DocPreview step={step} form={form} />
          </div>
        </div>
      </div>

      <Footer />
    </div>
  )
}
