// ── Helpers ───────────────────────────────────────────────────────────────────

function isValidEmail(v) { return /^[^\s@]+@[^\s@]+\.[^\s@]+$/.test(v.trim()) }

function triggerPDFDownload(disc, recv) {
  const text = `NON-DISCLOSURE AGREEMENT\n\nThis agreement is entered into between ${disc || '[Disclosing party name]'} and ${recv || '[Receiving party name]'}.\n\nThis document was generated by LegalShield. For prototype purposes only.`
  const blob = new Blob([text], { type: 'application/pdf' })
  const url = URL.createObjectURL(blob)
  const a = document.createElement('a')
  a.href = url; a.download = 'NDA_JaneSmith_AcmeCorp.pdf'
  document.body.appendChild(a); a.click(); document.body.removeChild(a)
  URL.revokeObjectURL(url)
}

// ── Stepper ───────────────────────────────────────────────────────────────────

function StepCheckIcon() {
  return <svg width="12" height="12" viewBox="0 0 12 12" fill="none"><polyline points="2,6.5 5,9.5 10,3" stroke="white" strokeWidth="2" strokeLinecap="round" strokeLinejoin="round"/></svg>
}

function Stepper({ step }) {
  return (
    <div className={styles.stepper}>
      {STEP_LABELS.map((label, i) => {
        const n = i + 1
        const done = n < step
        const active = n === step
        return (
          <Fragment key={label}>
            {i > 0 && <div className={`${styles.stepLine} ${n <= step ? styles.stepLineFilled : ''}`} />}
            <div className={styles.stepItem}>
              <div className={`${styles.stepCircle} ${done || active ? styles.stepCircleDark : ''}`}>
                {done ? <StepCheckIcon /> : <span>{n}</span>}
              </div>
              <span className={`${styles.stepLabel} ${active ? styles.stepLabelActive : ''} ${done ? styles.stepLabelDone : ''}`}>{label}</span>
            </div>
          </Fragment>
        )
      })}
    </div>
  )
}

