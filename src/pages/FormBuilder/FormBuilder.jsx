// ── Field primitives ──────────────────────────────────────────────────────────

function FieldWrap({ label, helper, children }) {
  return (
    <div className={styles.fieldWrap}>
      {label && <label className={styles.fieldLabel}>{label}</label>}
      {children}
      {helper && <p className={styles.fieldHelper}>{helper}</p>}
    </div>
  )
}

function TextInput({ label, placeholder, helper, value, onChange }) {
  return (
    <FieldWrap label={label} helper={helper}>
      <input
        type="text"
        className={`${styles.input} ${value ? styles.inputFilled : ''}`}
        placeholder={placeholder}
        value={value}
        onChange={e => onChange(e.target.value)}
      />
    </FieldWrap>
  )
}

function SelectInput({ label, placeholder, helper, value, onChange, options }) {
  return (
    <FieldWrap label={label} helper={helper}>
      <select
        className={`${styles.input} ${styles.select} ${value ? styles.inputFilled : ''}`}
        value={value}
        onChange={e => onChange(e.target.value)}
      >
        <option value="">{placeholder || 'Select…'}</option>
        {options.map(o => <option key={o} value={o}>{o}</option>)}
      </select>
    </FieldWrap>
  )
}

function TextareaInput({ label, placeholder, helper, value, onChange }) {
  return (
    <FieldWrap label={label} helper={helper}>
      <textarea
        className={`${styles.input} ${styles.textarea} ${value ? styles.inputFilled : ''}`}
        placeholder={placeholder}
        value={value}
        onChange={e => onChange(e.target.value)}
        rows={4}
      />
    </FieldWrap>
  )
}

function CheckboxGroup({ label, helper, options, selected, onChange }) {
  function toggle(opt) {
    onChange(selected.includes(opt) ? selected.filter(v => v !== opt) : [...selected, opt])
  }
  return (
    <FieldWrap label={label} helper={helper}>
      <div className={styles.checkGroup}>
        {options.map(opt => (
          <label key={opt} className={styles.checkRow}>
            <input
              type="checkbox"
              className={styles.checkInput}
              checked={selected.includes(opt)}
              onChange={() => toggle(opt)}
            />
            <span>{opt}</span>
          </label>
        ))}
      </div>
    </FieldWrap>
  )
}

function RadioGroup({ label, helper, options, value, onChange }) {
  return (
    <FieldWrap label={label} helper={helper}>
      <div className={styles.radioGroup}>
        {options.map(opt => (
          <label key={opt} className={styles.radioRow}>
            <input
              type="radio"
              className={styles.radioInput}
              checked={value === opt}
              onChange={() => onChange(opt)}
            />
            <span>{opt}</span>
          </label>
        ))}
      </div>
    </FieldWrap>
  )
}

